     تتضمن جراحة إزالة أورام الدماغ مخاطر مثل العدوى والنزيف. قد تعتمد المخاطر الأخرى على الجزء الدماغي 
      الذي يقع فيه الورم. فعلى سبيل المثال قد تتضمن جراحة الورم
      الذي يقع بجوار الأعصاب المتصلة بالعَيْن خطر فقدان البصر

