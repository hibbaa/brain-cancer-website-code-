      إذا كان ورم الدماغ يقع في مكان يسهُل الوصول إليه لإجراء عملية، فسيعمل الجراح على إزالة 
      أكبر قدر ممكن من ورم الدماغ في بعض الحالات تكون الأورام صغيرة وسهلة الفصل عن النسيج المحيط
      للمخ مما يجعل إجراء استئصال جراحي كامل  أمرًا ممكنًا وفي حالات أخرى لا يمكن فصل الأورام
